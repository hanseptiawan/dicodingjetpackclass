    public static ContentEntity getDetailContent(int contentID){
        ContentEntity newContentEntity = new ContentEntity();
        List<ContentEntity> contentEntities = new ArrayList<>();
        contentEntities.addAll(getMovieDummies());
        contentEntities.addAll(getTVShowDummies());

        for (ContentEntity entity : contentEntities){
            if (entity.getIdContent() == contentID){
                newContentEntity = entity;
            }
        }

        return newContentEntity;
    }

    public static List<ContentEntity> searchContent(String query) {
        List<ContentEntity> searchResults = new ArrayList<>();
        List<ContentEntity> contentEntities = new ArrayList<>();
        contentEntities.addAll(getMovieDummies());
        contentEntities.addAll(getTVShowDummies());

        for (ContentEntity contentEntity : contentEntities) {
            if (contentEntity.getTitle().toLowerCase().contains(query.toLowerCase())) {
                searchResults.add(contentEntity);
            }
        }
        return searchResults;
    }

